const mongoose = require("mongoose")

// schema 
const UserSchema = mongoose.Schema({
    name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    }
}, { timestamps: true })

// model
const User = mongoose.model('User', UserSchema)

module.exports = User